import 'package:flutter/material.dart';
import 'package:livros/exceptions/http_exception.dart';
import 'package:livros/models/book.dart';
import 'package:livros/models/book_list.dart';
import 'package:livros/utils/app_routes.dart';
import 'package:provider/provider.dart';

class BookItem extends StatelessWidget {
  final Book book;

  const BookItem(
    this.book, {
    Key? key,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    final msg = ScaffoldMessenger.of(context);
    return ListTile(
      title: Text(book.name),
      trailing: SizedBox(
        width: 100,
        child: Row(
          children: [
            IconButton(
              icon: const Icon(Icons.edit),
              color: Theme.of(context).colorScheme.primary,
              onPressed: () {
                Navigator.of(context).pushNamed(
                  AppRoutes.bookForm,
                  arguments: book,
                );
              },
            ),
            IconButton(
              icon: const Icon(Icons.delete),
              color: Theme.of(context).errorColor,
              onPressed: () {
                showDialog<bool>(
                  context: context,
                  builder: (ctx) => AlertDialog(
                    title: const Text('Excluir Livro'),
                    content: const Text('Tem certeza?'),
                    actions: [
                      TextButton(
                        child: const Text('NÃ£o'),
                        onPressed: () => Navigator.of(ctx).pop(false),
                      ),
                      TextButton(
                        child: const Text('Sim'),
                        onPressed: () => Navigator.of(ctx).pop(true),
                      ),
                    ],
                  ),
                ).then((value) async {
                  if (value ?? false) {
                    try {
                      await Provider.of<BookList>(
                        context,
                        listen: false,
                      ).removeBook(book);
                    } on HttpException catch (error) {
                      msg.showSnackBar(
                        SnackBar(
                          content: Text(error.toString()),
                        ),
                      );
                    }
                  }
                });
              },
            ),
          ],
        ),
      ),
    );
  }
}
